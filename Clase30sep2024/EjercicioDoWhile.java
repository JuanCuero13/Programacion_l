public class EjercicioDoWhile {
    public static void main(String[] args) {

        String cad = "Programacion I";
        int j = 1;
        do {
            System.out.println(cad);
            j++;
        } while (j > 5);

        for (int i = 1; j > 5; i++) {
            System.out.println(cad);
        }
    }
}
